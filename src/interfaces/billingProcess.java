    public void cashierIdSet()
    {
        try {
            String sql="select cashier_id from cashiers where uname='"+cashierUserName+"'";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next())
            {
                cashierId=Integer.parseInt(rs.getString(1));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"get cashier id fail");
        }
    }
    
    public void loadTable1()
    {
        try {
            String sql="select * from medicine where quantity>0 and expire_date>=current_date()";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            table1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"table load fail");
        }
    }
    
