    private void add1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add1ActionPerformed
        if((id.getText().length()!=0)&&(qty2.getText().length()!=0))
        {
            if("no".equals(invoiceRunning))
            {
                int check=JOptionPane.showConfirmDialog(null,"Create Invoice");
                if(check==0)
                {
                    invoiceRunning="yes";
                    try {
                        String sql="insert into invoice values(default,'"+cashierId+"',current_date())";
                        pst=conn.prepareStatement(sql);
                        pst.execute();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null,"invoice create fail");
                    }
                    
                    try {
                        String sql="select last_insert_id()";
                        pst=conn.prepareStatement(sql);
                        rs=pst.executeQuery();
                        while(rs.next())
                        {
                            invoiceId=Integer.parseInt(rs.getString(1));
                        }
                        System.out.println(invoiceId);
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null,"get invoice id failed");
                    }
                }
            }
            
            if("yes".equals(invoiceRunning))
            {
                if(Integer.parseInt(qty.getText())<Integer.parseInt(qty2.getText()))
                {
                    JOptionPane.showMessageDialog(null,"check quantity");
                    qty2.setText("");
                }
                else
                {
                    int Id=Integer.parseInt(id.getText());
                    int Qty=Integer.parseInt(qty2.getText());
                    float oneQtyPrice=Float.parseFloat(price.getText());
                    float Price=oneQtyPrice*Qty;
                    float Total=Float.parseFloat(total.getText());
                    
                    try {
                        String sql="insert into transaction values(last_insert_id(),'"+Id+"','"+Qty+"','"+Price+"')";
                        pst=conn.prepareStatement(sql);
                        pst.execute();
                        loadTable1();
                        loadTable2();
                        clear();
                        Total=Total+Price;
                        total.setText(String.valueOf(Total));   
                                
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null,"Transaction fail");
                    }
                }
            }
        }
    }//GEN-LAST:event_add1ActionPerformed

