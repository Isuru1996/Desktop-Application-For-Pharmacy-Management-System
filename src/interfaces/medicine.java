    public void tableData()
    {
        int row=table1.getSelectedRow();
        
        id.setText(table1.getValueAt(row,0).toString());
        name.setText(table1.getValueAt(row, 1).toString());
        qty.setText(table1.getValueAt(row, 2).toString());
        price.setText(table1.getValueAt(row, 3).toString());
        manuDate.setText(table1.getValueAt(row, 4).toString());
        exDate.setText(table1.getValueAt(row, 5).toString());
        
        add.setVisible(false);
        
    }
    

