    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
       
        String Name=null;
        int Qty=0;
        float Price=0;
        String ManuDate=null;
        String ExDate=null;
        
        if((name.getText().length()==0)||(qty.getText().length()==0)||(price.getText().length()==0)||(manuDate.getText().length()==0)||(exDate.getText().length()==0))
        {
            JOptionPane.showMessageDialog(null,"Data added failed");
        }
        else
        {
            Name=name.getText();
            //System.out.println(Name);
            
            try {
                Qty=Integer.parseInt(qty.getText().toString());
                //System.out.println(Qty);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,"please check quantity");
            }
            
            try {
                Price=Float.parseFloat(price.getText().toString());
                //System.out.println(Price);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,"please check price");
            }
            
            
            ManuDate=manuDate.getText();
            //System.out.println(ManuDate);
            
            ExDate=exDate.getText();
            //System.out.println(ExDate); 
        }
        
        if((Name!=null)&&(Qty!=0)&&(Price!=0)&&(ManuDate!=null)&&(ExDate!=null))
        {
            try {
                String sql="insert into medicine values(default,'"+Name+"','"+Qty+"','"+Price+"','"+ManuDate+"','"+ExDate+"')";
                pst=conn.prepareStatement(sql);
                pst.execute();
                tableLoad();
                clear();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,"add fail");
            }
        }
        
    }//GEN-LAST:event_addActionPerformed

